public class Persons {
  
  private String name;
  private String phone_number;

  public Persons(String name, String phone_number) {
    this.name = name;
    this.phone_number = phone_number;
  }

  public String getName() {
    return this.name;
  }

  public String getPhoneNumber() {
    return this.phone_number;
  }

  

}